const arr = Array.of(1, 2, 3);
console.log(arr);
// [ 1, 2, 3 ]

const persons = Array.of('Márcio', 'Adriano', 'Luiza');
console.log(persons);
// [ 'Márcio', 'Adriano', 'Luiza' ]

const numbersAndStrings = Array.of(1, 2, 'strings', 'texto');
console.log(numbersAndStrings);
// [ 1, 2, 'strings', 'texto' ]